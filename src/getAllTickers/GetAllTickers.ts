// Formats the tickers fetched from the API
export function formatTickers(tickers: Ticker[]): string[] {
  try {
    const tickerList = tickers.map((e) => e.ticker);
    const uniqueTickers = Array.from(new Set(tickerList));
    return uniqueTickers;
  } catch (error) {
    console.error("Error formatting tickers:", error);
    throw error;
  }
}

// Main function to get all formatted tickers
export default async function getAllTickers(): Promise<string[]> {
  try {
    const tickersData = await fetchTickers();
    const formattedTickers = formatTickers(tickersData);
    return formattedTickers;
  } catch (error) {
    console.error("Error getting all tickers:", error);
    throw error;
  }
}
